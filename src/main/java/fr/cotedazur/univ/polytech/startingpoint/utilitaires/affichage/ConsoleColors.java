package fr.cotedazur.univ.polytech.startingpoint.utilitaires.affichage;

public class ConsoleColors {
    // Reset
    public static final String RESET = "\033[0m";  // Text Reset

    // Regular Colors
    public static final String GREEN = "\033[0;32m";   // VERT
    public static final String YELLOW = "\033[0;33m";  // JAUNE
    public static final String PURPLE = "\033[0;35m";  // ROSE (Magenta)
    public static final String BLUE = "\033[0;34m";    // BLEU (pour les canaux par ex)
    public static final String CYAN = "\033[0;36m";    // CYAN
}